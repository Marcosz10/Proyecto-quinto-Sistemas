    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int fila=tablaDatos.getSelectedRow();
        String valor=tablaDatos.getValueAt(fila, 0).toString();

        if (fila >= 0) {

            try {
                PreparedStatement pps = cn.prepareStatement("DELETE FROM usuario WHERE id='"+valor+"'");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato Eliminado");
                mostrartabla("");

            } catch (SQLException ex) {
                Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        try {
            PreparedStatement pps = cn.prepareStatement("UPDATE usuario SET nombre='"+txtNombre.getText()+"', "
                    + "apellido='"+txtApellidos.getText()+"', edad='"+txtEdad.getText()+"', "
                    + "correo='"+txtCorreo.getText()+"' "
                    + ",Fecha='"+txtFecha.getText()+"' WHERE id='"+txtBuscar.getText()+"'");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "datos actualizados");
            limpiar();
            mostrartabla("");
            bloquear();

        } catch (SQLException ex) {
            Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        int fila=tablaDatos.getSelectedRow();

        if (fila >= 0) {
            txtBuscar.setText(tablaDatos.getValueAt(fila, 0).toString());
            txtNombre.setText(tablaDatos.getValueAt(fila, 1).toString());
            txtApellidos.setText(tablaDatos.getValueAt(fila, 2).toString());
            txtEdad.setText(tablaDatos.getValueAt(fila, 3).toString());
            txtCorreo.setText(tablaDatos.getValueAt(fila, 4).toString());
            txtFecha.setText(tablaDatos.getValueAt(fila, 5).toString());
            
            desbloquear();
            btnLimpiar.setEnabled(true);
            btnActualizar.setEnabled(true);
            btnNuevo.setEnabled(false);
            Fechas();
                    
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Fila no Seleccionada");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

